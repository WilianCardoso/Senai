    private void jBtIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtIgualActionPerformed

        // TODO add your handling code here:
        num2 = Double.parseDouble(jtfVisor.getText());

        if (jtfVisor.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Nenhuma operação solicitada!");//Verifica se usa o igual com o visor vazio

        } else {

            switch (op) {

                case '+':

                    num2 = Double.parseDouble(jtfVisor.getText());//Armazena o segundo numero

                    jtfVisor.setText(String.valueOf(num1 + num2));//Faz a operação

                    break;

                case '-':

                    num2 = Double.parseDouble(jtfVisor.getText());

                    jtfVisor.setText(String.valueOf(num1 - num2));

                    break;

                case '*':

                    num2 = Double.parseDouble(jtfVisor.getText());

                    jtfVisor.setText(String.valueOf(num1 * num2));

                    break;

                case '/':

                    if (num2 == 0) { //Verifica ha possibilidade de fazer a divisao

                        JOptionPane.showMessageDialog(null, "Não é possivel fazer uma divisão por zero");

                    } else {

                        num2 = Double.parseDouble(jtfVisor.getText());
                        jtfVisor.setText(String.valueOf(num1 / num2));
                    }
                    break;
                case '%':
                    num2 = Double.parseDouble(jtfVisor.getText());
                    //faz a multiplicação e a divisao, depois a conversao
                    double resultado = (num1 / 100) * 2;
                    jtfVisor.setText(String.valueOf(resultado));
                    break;
                default:
                    throw new AssertionError();
            }
        }
    }//GEN-LAST:event_jBtIgualActionPerformed


