    private void jBtMemRecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtMemRecActionPerformed

        // TODO add your handling code here:
// Nome do arquivo onde as contas estão salvas
        String nomeArquivo = "memoria_calculadora.txt";

        // StringBuilder para armazenar todas as contas lidas do arquivo
        StringBuilder contasSalvas = new StringBuilder();

        // Tenta ler as contas do arquivo
        try (BufferedReader reader = new BufferedReader(new FileReader(nomeArquivo))) {
            String linha;

            // Lê cada linha do arquivo
            while ((linha = reader.readLine()) != null) {
                // Adiciona a linha ao StringBuilder
                contasSalvas.append(linha).append("\n");
            }

            // Verifica se há algo no arquivo
            if (contasSalvas.length() > 0) {
                // Exibe todas as contas no visor ou em um JOptionPane
                jtfVisor.setText(contasSalvas.toString());
            } else {
                JOptionPane.showMessageDialog(null, "Nenhuma conta foi salva na memória.");
            }
        } catch (IOException e) {
            // Exibe um erro se houver problemas ao ler o arquivo
            JOptionPane.showMessageDialog(null, "Erro ao ler a memória: " + e.getMessage());
        }
    }//GEN-LAST:event_jBtMemRecActionPerformed

